const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export function LanguageProvider({ children }: { children: React.ReactNode }) {
  const [language, setLanguage] = useState<Language>(() => {
    const saved = localStorage.getItem('language');
    if (saved && Object.keys(translations).includes(saved)) {
      return saved as Language;
    }
    // Auto-detect browser language
    const browserLang = navigator.language.split('-')[0];
    const detectedLang = Object.keys(translations).includes(browserLang) ? browserLang as Language : 'en';
    return detectedLang;
  });

  useEffect(() => {
    localStorage.setItem('language', language);
  }, [language]);

  const t = (key: string): string => {
    const currentTranslations = translations[language as keyof typeof translations];
    
    // Direct lookup in the current language translations
    const translation = (currentTranslations as Record<string, string>)?.[key];
    
    // If translation found in current language, return it
    if (translation) {
      return translation;
    }
    
    // Fallback to English if translation not found in current language
    if (language !== 'en') {
      const englishTranslations = translations.en as Record<string, string>;
      const englishTranslation = englishTranslations[key];
      if (englishTranslation) {
        return englishTranslation;
      }
    }
    
    // If no translation found in any language, return the key
    return key;
  };

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage() {
  const context = useContext(LanguageContext);
  if (!context) {
    throw new Error('useLanguage must be used within a LanguageProvider');
  }
  return context;
}